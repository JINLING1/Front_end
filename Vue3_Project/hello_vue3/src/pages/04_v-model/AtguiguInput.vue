<template>
  <input 
    type="text" 
    :value="ming"
    @input="emit('update:ming',(<HTMLInputElement>$event.target).value)"
  >
  <br>
  <!-- 对于原生事件，$event就是事件对象 input -->
  <input 
    type="text" 
    :value="mima"
    @input="emit('update:mima',(<HTMLInputElement>$event.target).value)"
  >
</template>

<script setup lang="ts" name="AtguiguInput">
  defineProps(['ming','mima'])//父组件向子组件传递数据
  const emit = defineEmits(['update:ming','update:mima'])//接收父组件的自定义事件
</script>

